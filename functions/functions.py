def find_type_arguments (arg1,arg2):
	""" Prints the type of passed two arguments
		Args:
			arg1:  Any value 
			arg2:  Any value

		Returns:
			No return value
	"""

	print ("Argument 1 :", type(arg1))
	print ("Argument 2 :", type(arg2))

input1 = input("Enter input1 : ")
input2 = input("Enter input2 : ")

find_type_arguments (input1, input2)
